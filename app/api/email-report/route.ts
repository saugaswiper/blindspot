export async function POST(request: Request) {
  try {
    // Check Resend is configured
    const resendKey = process.env.RESEND_API_KEY;
    if (!resendKey) {
      return Response.json(
        { error: "Email delivery is not configured. Please contact the site administrator." },
        { status: 503 }
      );
    }

    // Auth check
    const supabase = await createClient();
    const {
      data: { user },
    } = await supabase.auth.getUser();
    if (!user) {
      return Response.json({ error: "Please sign in to email a report." }, { status: 401 });
    }

    // Validate input
    const body = await request.json() as unknown;
    const parsed = emailReportSchema.safeParse(body);
    if (!parsed.success) {
      const firstIssue = parsed.error.issues[0];
      return Response.json({ error: firstIssue?.message ?? "Invalid request." }, { status: 400 });
    }
    const { resultId, email } = parsed.data;

    // Fetch result (RLS ensures it belongs to this user)
    const { data: result, error: fetchError } = await supabase
      .from("search_results")
      .select(`
        id,
        primary_study_count,
        existing_reviews,
        feasibility_score,
        feasibility_explanation,
        gap_analysis,
        study_design_recommendation,
        searches (query_text)
      `)
      .eq("id", resultId)
      .single();

    if (fetchError || !result) {
      return Response.json({ error: "Result not found." }, { status: 404 });
    }

    const query =
      (result.searches as unknown as { query_text: string } | null)?.query_text ?? "Research topic";
    const primaryStudyCount = result.primary_study_count as number;
    const existingReviews = (result.existing_reviews ?? []) as ExistingReview[];
    const feasibilityScore = result.feasibility_score as FeasibilityScore | null;
    const feasibilityExplanation = result.feasibility_explanation as string | null;
    const gapAnalysis = result.gap_analysis as GapAnalysis | null;
    const studyDesign = result.study_design_recommendation as StudyDesignRecommendation | null;

    // Build email content
    const reportParams = {
      query,
      primaryStudyCount,
      existingReviews,
      feasibilityScore,
      feasibilityExplanation,
      gapAnalysis,
      studyDesign,
    };

    const htmlBody = buildHtmlEmail(reportParams);
    const plainText = buildPlainTextReport(reportParams);

    // Encode plain-text report as base64 attachment
    const attachmentContent = Buffer.from(plainText, "utf-8").toString("base64");
    const safeTitle = query.slice(0, 40).replace(/[^a-zA-Z0-9 _-]/g, "").trim().replace(/\s+/g, "_");
    const attachmentFilename = `blindspot_report_${safeTitle || "report"}.txt`;

    // Send via Resend REST API (no npm package needed)
    const resendResponse = await fetch("https://api.resend.com/emails", {
      method: "POST",
      headers: {
        Authorization: `Bearer ${resendKey}`,
        "Content-Type": "application/json",
      },
      body: JSON.stringify({
        from: "Blindspot <reports@blindspot.app>",
        to: [email],
        subject: `Your Blindspot report: ${query.slice(0, 60)}`,
        html: htmlBody,
        text: plainText,
        attachments: [
          {
            filename: attachmentFilename,
            content: attachmentContent,
          },
        ],
      }),
    });

    if (!resendResponse.ok) {
      const errBody = await resendResponse.json().catch(() => ({})) as { message?: string };
      console.error("[email-report] Resend error:", resendResponse.status, errBody);
      return Response.json(
        { error: "Failed to send email. Please try again later." },
        { status: 502 }
      );
    }

    return Response.json({ success: true });
  } catch (error) {
    const apiError = toApiError(error);
    return Response.json({ error: apiError.userMessage }, { status: apiError.statusCode });
  }
}
